package org.example;

public class DoubleComparison {
        public static void main(String[] args) {

            double firstValue = 12.5;
            double secondValue = 8.7;


            if (firstValue > secondValue) {

                System.out.println("Double value " + firstValue + " is larger than " + secondValue + ".");
            } else {

                System.out.println("Double value " + firstValue + " is not larger than " + secondValue + ".");
            }
        }
    }


