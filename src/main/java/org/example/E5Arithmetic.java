package org.example;

public class E5Arithmetic {

    public static void main(String[] args) {

        int num1=20;
        int num2=10;

        // Addition
        System.out.println(num1+num2);
        // Subtraction
        System.out.println(num1-num2);
        //Multiplication
        System.out.println(num1*num2);
        //Division
        System.out.println(num1/num2);
        // Modulus
        System.out.println(num1%num2);
        System.out.println(15.2%12.5);
    }
}
