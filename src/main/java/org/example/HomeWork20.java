package org.example;

public class HomeWork20 {

    public static void main(String[] args) {
        int[] numbers = {10, 20, 30, 40, 50};
        int sum = 0;

        // Calculate the sum using a for loop
        for (int i = 0; i < numbers.length; i++) {
            sum += numbers[i];
        }

        System.out.println( sum);
    }
}

