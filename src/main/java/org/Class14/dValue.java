package org.Class14;

public class dValue {


        // Static method with int return type
        public static int doubleValue(int number) {
            return number * 2;
        }

        public static void main(String[] args) {
            // Example usage
            int inputNumber = 10;
            int result = doubleValue(inputNumber);

            System.out.println( inputNumber);
            System.out.println( result);
        }
    }


